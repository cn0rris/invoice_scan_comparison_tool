(function () {
  const SPLIT_STORAGE_KEY = "invoiceDetailSplitPercent";
  const DEFAULT_PERCENT = 60;
  const MIN_PERCENT = 20;
  const MAX_PERCENT = 80;

  async function loadGroundTruth() {
    const pre = document.getElementById("ground-truth-content");
    if (!pre) return; // no ground-truth pane rendered (status was "missing")
    try {
      const resp = await fetch(`/api/ground-truth/${window.GROUND_TRUTH_FILENAME_URL}`);
      const text = await resp.text();
      try {
        pre.textContent = JSON.stringify(JSON.parse(text), null, 2);
      } catch (e) {
        pre.textContent = text; // not valid JSON syntax — show it raw so it's still inspectable
      }
    } catch (e) {
      pre.textContent = `Failed to load ground truth: ${e}`;
    }
  }

  function initSplitDrag() {
    const container = document.getElementById("detail-split");
    const invoicePane = document.getElementById("invoice-pane");
    const divider = document.getElementById("split-divider");
    if (!container || !invoicePane || !divider) return;

    // The initial split percentage is applied synchronously in an inline
    // <script> in invoice_detail.html, before the iframe/img is parsed — not
    // here. Re-applying it on DOMContentLoaded would run too late: the PDF
    // viewer inside the iframe computes its "fit" zoom once, at load time,
    // and does not reflow when the pane is resized afterward.
    function applyPercent(percent) {
      invoicePane.style.flexBasis = `${percent}%`;
    }

    let dragging = false;

    function percentFromEvent(e) {
      const rect = container.getBoundingClientRect();
      const raw = ((e.clientX - rect.left) / rect.width) * 100;
      return Math.min(MAX_PERCENT, Math.max(MIN_PERCENT, raw));
    }

    function onPointerMove(e) {
      if (!dragging) return;
      applyPercent(percentFromEvent(e));
    }

    function onPointerUp(e) {
      if (!dragging) return;
      dragging = false;
      divider.classList.remove("dragging");
      document.body.style.userSelect = "";
      localStorage.setItem(SPLIT_STORAGE_KEY, String(percentFromEvent(e)));
      window.removeEventListener("pointermove", onPointerMove);
      window.removeEventListener("pointerup", onPointerUp);
    }

    divider.addEventListener("pointerdown", (e) => {
      dragging = true;
      divider.classList.add("dragging");
      document.body.style.userSelect = "none"; // avoid selecting page text while dragging
      window.addEventListener("pointermove", onPointerMove);
      window.addEventListener("pointerup", onPointerUp);
      e.preventDefault();
    });

    divider.addEventListener("dblclick", () => {
      applyPercent(DEFAULT_PERCENT);
      localStorage.removeItem(SPLIT_STORAGE_KEY);
    });

    // Keyboard support to match the divider's role="separator"
    divider.addEventListener("keydown", (e) => {
      const current = parseFloat(invoicePane.style.flexBasis) || DEFAULT_PERCENT;
      if (e.key === "ArrowLeft") {
        applyPercent(Math.max(MIN_PERCENT, current - 2));
        localStorage.setItem(SPLIT_STORAGE_KEY, invoicePane.style.flexBasis);
      } else if (e.key === "ArrowRight") {
        applyPercent(Math.min(MAX_PERCENT, current + 2));
        localStorage.setItem(SPLIT_STORAGE_KEY, invoicePane.style.flexBasis);
      }
    });
  }

  // --- Candidate ground truth workflow (generate -> review/edit -> approve) ---

  function setCandidateStatus(message, cls) {
    const el = document.getElementById("candidate-status");
    if (!el) return;
    el.innerHTML = message ? `<p class="${cls}">${message}</p>` : "";
  }

  function showCandidateMeta(meta) {
    const el = document.getElementById("candidate-meta");
    if (!el) return;
    if (meta && meta.model_id) {
      const when = meta.generated_at ? new Date(meta.generated_at).toLocaleString() : "unknown time";
      el.textContent = `Generated by ${meta.model_id} on ${when}. Verify every value against the invoice before approving.`;
    } else {
      el.textContent = "Verify every value against the invoice before approving.";
    }
  }

  async function loadModelOptions() {
    const select = document.getElementById("candidate-model-select");
    if (!select) return;
    try {
      const resp = await fetch("/api/models");
      const data = await resp.json();
      const options = [];
      for (const m of data.anthropic) options.push(`<option value="${m}">${m}</option>`);
      if (data.ollama.available) {
        for (const m of data.ollama.models) options.push(`<option value="${m}">${m} (local)</option>`);
      }
      select.innerHTML = options.join("");
    } catch (e) {
      select.innerHTML = '<option value="">failed to load models</option>';
    }
  }

  async function loadCandidate() {
    try {
      const resp = await fetch(`/api/invoices/${window.INVOICE_FILENAME_URL}/candidate`);
      if (!resp.ok) return;
      const data = await resp.json();
      document.getElementById("candidate-editor").value = data.content;
      showCandidateMeta(data.meta);
    } catch (e) {
      setCandidateStatus(`Failed to load candidate: ${e}`, "status-error");
    }
  }

  function showEditor() {
    document.getElementById("generate-section").hidden = true;
    document.getElementById("candidate-editor-section").hidden = false;
  }

  async function generateCandidate() {
    const btn = document.getElementById("generate-candidate-btn");
    const model = document.getElementById("candidate-model-select").value;
    if (!model) {
      setCandidateStatus("Select a model first.", "status-error");
      return;
    }
    btn.disabled = true;
    setCandidateStatus(`Extracting with ${model}… this can take a minute.`, "muted");
    try {
      const resp = await fetch(`/api/invoices/${window.INVOICE_FILENAME_URL}/candidate`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ model_id: model }),
      });
      const data = await resp.json();
      if (!resp.ok) {
        setCandidateStatus(`Generation failed: ${data.detail || resp.status}`, "status-error");
        return;
      }
      document.getElementById("candidate-editor").value = data.content;
      showCandidateMeta(data.meta);
      showEditor();
      setCandidateStatus("", "");
    } catch (e) {
      setCandidateStatus(`Generation failed: ${e}`, "status-error");
    } finally {
      btn.disabled = false;
    }
  }

  async function saveCandidate() {
    const content = document.getElementById("candidate-editor").value;
    try {
      const resp = await fetch(`/api/invoices/${window.INVOICE_FILENAME_URL}/candidate`, {
        method: "PUT",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ content }),
      });
      const data = await resp.json();
      if (!resp.ok) {
        setCandidateStatus(`Save failed: ${data.detail || resp.status}`, "status-error");
        return;
      }
      setCandidateStatus("Draft saved.", "status-success");
    } catch (e) {
      setCandidateStatus(`Save failed: ${e}`, "status-error");
    }
  }

  async function approveCandidate() {
    const content = document.getElementById("candidate-editor").value;
    try {
      const resp = await fetch(`/api/invoices/${window.INVOICE_FILENAME_URL}/candidate/approve`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ content }),
      });
      const data = await resp.json();
      if (!resp.ok) {
        setCandidateStatus(`Approval failed: ${data.detail || resp.status}`, "status-error");
        return;
      }
      window.location.reload(); // page re-renders in the approved (read-only) state
    } catch (e) {
      setCandidateStatus(`Approval failed: ${e}`, "status-error");
    }
  }

  async function discardCandidate() {
    if (!window.confirm("Discard this candidate ground truth? This cannot be undone.")) return;
    try {
      const resp = await fetch(`/api/invoices/${window.INVOICE_FILENAME_URL}/candidate`, { method: "DELETE" });
      if (!resp.ok) {
        const data = await resp.json();
        setCandidateStatus(`Discard failed: ${data.detail || resp.status}`, "status-error");
        return;
      }
      window.location.reload(); // page re-renders in the "missing" state with generate controls
    } catch (e) {
      setCandidateStatus(`Discard failed: ${e}`, "status-error");
    }
  }

  function initCandidateWorkflow() {
    const generateBtn = document.getElementById("generate-candidate-btn");
    if (!generateBtn) return; // approved ground truth exists — no candidate UI on this page
    loadModelOptions();
    if (window.GROUND_TRUTH_STATUS === "candidate") loadCandidate();
    generateBtn.addEventListener("click", generateCandidate);
    document.getElementById("save-candidate-btn").addEventListener("click", saveCandidate);
    document.getElementById("approve-candidate-btn").addEventListener("click", approveCandidate);
    document.getElementById("discard-candidate-btn").addEventListener("click", discardCandidate);
  }

  // --- Direct editing of already-approved (or invalid) ground truth ---

  function initGroundTruthEditor() {
    const editBtn = document.getElementById("edit-ground-truth-btn");
    if (!editBtn) return; // no approved/invalid ground truth on this page (missing/candidate state)

    const viewSection = document.getElementById("ground-truth-view-section");
    const editorSection = document.getElementById("ground-truth-editor-section");
    const textarea = document.getElementById("ground-truth-editor");
    const saveBtn = document.getElementById("save-ground-truth-btn");
    const statusEl = document.getElementById("ground-truth-edit-status");

    function setStatus(message, cls) {
      statusEl.innerHTML = message ? `<p class="${cls}">${message}</p>` : "";
    }

    editBtn.addEventListener("click", async () => {
      try {
        const resp = await fetch(`/api/ground-truth/${window.GROUND_TRUTH_FILENAME_URL}`);
        const text = await resp.text();
        try {
          textarea.value = JSON.stringify(JSON.parse(text), null, 2);
        } catch (e) {
          textarea.value = text; // invalid JSON syntax — edit it raw as-is
        }
        viewSection.hidden = true;
        editorSection.hidden = false;
        setStatus("", "");
      } catch (e) {
        setStatus(`Failed to load ground truth for editing: ${e}`, "status-error");
      }
    });

    document.getElementById("cancel-edit-ground-truth-btn").addEventListener("click", () => {
      editorSection.hidden = true;
      viewSection.hidden = false;
      setStatus("", "");
    });

    saveBtn.addEventListener("click", async () => {
      saveBtn.disabled = true;
      try {
        const resp = await fetch(`/api/invoices/${window.INVOICE_FILENAME_URL}/ground-truth`, {
          method: "PUT",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ content: textarea.value }),
        });
        const data = await resp.json();
        if (!resp.ok) {
          setStatus(`Save failed: ${data.detail || resp.status}`, "status-error");
          return;
        }
        window.location.reload(); // re-render read-only view; also clears "invalid" warning if fixed
      } catch (e) {
        setStatus(`Save failed: ${e}`, "status-error");
      } finally {
        saveBtn.disabled = false;
      }
    });
  }

  document.addEventListener("DOMContentLoaded", () => {
    loadGroundTruth();
    initSplitDrag();
    initCandidateWorkflow();
    initGroundTruthEditor();
  });
})();
